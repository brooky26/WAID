def test_log_likelihood_monotonically_nondecreasing_during_em():
    X, _, _ = make_two_regime_data()
    hmm = GaussianHMM(n_states=2, n_features=2, random_seed=3)
    hmm.fit(X, max_iterations=50, tolerance=1e-10)

    history = hmm.log_likelihood_history
    assert len(history) >= 2
    diffs = np.diff(history)
    assert np.all(diffs > -1e-6), f"log-likelihood decreased: {history}"


def test_fit_recovers_approximate_cluster_means():
    X, _, true_means = make_two_regime_data(n_per_regime=300, seed=5)
    hmm = GaussianHMM(n_states=2, n_features=2, random_seed=7)
    hmm.fit(X, max_iterations=100)

    fitted = hmm.means
    dist_00 = np.linalg.norm(fitted[0] - true_means[0])
    dist_01 = np.linalg.norm(fitted[0] - true_means[1])
    if dist_00 < dist_01:
        pairing = [0, 1]
    else:
        pairing = [1, 0]
    for i, true_idx in enumerate(pairing):
        assert np.linalg.norm(fitted[i] - true_means[true_idx]) < 0.3


def test_fit_raises_on_insufficient_data():
    hmm = GaussianHMM(n_states=4, n_features=2, random_seed=1)
    X = np.random.default_rng(0).normal(0, 1, size=(3, 2))
    with pytest.raises(ValueError, match="at least"):
        hmm.fit(X)


def test_fit_raises_on_wrong_feature_dimensionality():
    hmm = GaussianHMM(n_states=2, n_features=3, random_seed=1)
    X = np.random.default_rng(0).normal(0, 1, size=(20, 2))
    with pytest.raises(ValueError, match="shape"):
        hmm.fit(X)


def test_transition_matrix_rows_sum_to_one_after_fit():
    X, _, _ = make_two_regime_data()
    hmm = GaussianHMM(n_states=2, n_features=2, random_seed=1)
    hmm.fit(X, max_iterations=20)
    row_sums = hmm.A.sum(axis=1)
    np.testing.assert_allclose(row_sums, np.ones(2), atol=1e-8)


def test_variances_floored_at_min_variance():
    X, _, _ = make_two_regime_data()
    hmm = GaussianHMM(n_states=2, n_features=2, min_variance=0.05, random_seed=1)
    hmm.fit(X, max_iterations=20)
    assert np.all(hmm.variances >= 0.05 - 1e-9)


# --------------------------------------------------------------------- #
# Viterbi
# --------------------------------------------------------------------- #


def test_viterbi_recovers_dominant_state_structure():
    X, true_states, _ = make_two_regime_data(n_per_regime=300, seed=9)
    hmm = GaussianHMM(n_states=2, n_features=2, random_seed=1)
    hmm.fit(X, max_iterations=100)
    path = hmm.viterbi(X)

    agreement_direct = np.mean(path == true_states)
    agreement_flipped = np.mean(path == (1 - true_states))
    assert max(agreement_direct, agreement_flipped) > 0.85


def test_viterbi_returns_valid_state_indices():
    X, _, _ = make_two_regime_data()
    hmm = GaussianHMM(n_states=2, n_features=2, random_seed=1)
    hmm.fit(X, max_iterations=20)
    path = hmm.viterbi(X)
    assert path.min() >= 0
    assert path.max() < hmm.n_states
    assert len(path) == len(X)


