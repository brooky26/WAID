def test_forward_filter_probabilities_sum_to_one_each_timestep():
    X, _, _ = make_two_regime_data()
    hmm = GaussianHMM(n_states=2, n_features=2, random_seed=1)
    hmm.fit(X, max_iterations=20)
    filtered = hmm.forward_filter(X)
    sums = filtered.sum(axis=1)
    np.testing.assert_allclose(sums, np.ones(len(X)), atol=1e-8)


def test_smoothed_posteriors_sum_to_one_each_timestep():
    X, _, _ = make_two_regime_data()
    hmm = GaussianHMM(n_states=2, n_features=2, random_seed=1)
    hmm.fit(X, max_iterations=20)
    gamma = hmm.smoothed_posteriors(X)
    sums = gamma.sum(axis=1)
    np.testing.assert_allclose(sums, np.ones(len(X)), atol=1e-8)


def test_forward_step_matches_batch_forward_filter():
    """
    The whole point of forward_step: replaying observations one at a time
    via the incremental update must produce identical filtered
    probabilities to the batch forward pass over the same sequence.
    """
    X, _, _ = make_two_regime_data()
    hmm = GaussianHMM(n_states=2, n_features=2, random_seed=1)
    hmm.fit(X, max_iterations=20)

    batch_filtered = hmm.forward_filter(X)

    alpha = None
    streamed = []
    for t in range(len(X)):
        alpha, _ = hmm.forward_step(alpha, X[t])
        streamed.append(alpha)
    streamed = np.array(streamed)

    np.testing.assert_allclose(streamed, batch_filtered, atol=1e-8)


