import numpy as np
import pytest

from regime.hmm import GaussianHMM


def make_two_regime_data(n_per_regime=150, seed=0):
    """
    Synthetic 2-state data with well-separated Gaussian clusters in 2D,
    generated by an actual Markov chain with sticky transitions (so the
    HMM's temporal structure is meaningfully exercised, not just i.i.d.
    cluster membership).
    """
    rng = np.random.default_rng(seed)
    true_means = np.array([[-1.0, -1.0], [1.0, 1.0]])
    true_std = 0.3

    T = n_per_regime * 2
    states = np.zeros(T, dtype=int)
    states[0] = rng.integers(0, 2)
    stay_prob = 0.95
    for t in range(1, T):
        if rng.random() < stay_prob:
            states[t] = states[t - 1]
        else:
            states[t] = 1 - states[t - 1]

    X = np.array([rng.normal(true_means[s], true_std) for s in states])
    return X, states, true_means


