def test_emission_probs_sum_is_positive_and_finite():
    hmm = GaussianHMM(n_states=3, n_features=2, random_seed=1)
    x = np.array([0.1, -0.2])
    probs = hmm.emission_probs(x)
    assert len(probs) == 3
    assert np.all(np.isfinite(probs))
    assert np.all(probs >= 0)


def test_emission_probs_highest_for_nearest_mean():
    hmm = GaussianHMM(n_states=2, n_features=1, random_seed=1)
    hmm.means = np.array([[0.0], [10.0]])
    hmm.variances = np.array([[1.0], [1.0]])
    probs = hmm.emission_probs(np.array([0.1]))
    assert probs[0] > probs[1]


def test_emission_probs_no_underflow_on_extreme_values_with_small_variance():
    hmm = GaussianHMM(n_states=2, n_features=4, min_variance=1e-3, random_seed=1)
    hmm.means = np.zeros((2, 4))
    hmm.variances = np.full((2, 4), 1e-3)
    x = np.array([1.0, -1.0, 1.0, -1.0])
    probs = hmm.emission_probs(x)
    assert np.isclose(np.max(probs), 1.0)
    assert np.all(np.isfinite(probs))


